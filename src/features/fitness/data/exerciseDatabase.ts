/* ------------------------------------------------------------------ */
/*  Seed function                                                        */
/* ------------------------------------------------------------------ */
export async function seedExercises(db: DatabaseService): Promise<number> {
  const existing = await db.query<{ count: number }>(
    'SELECT COUNT(*) as count FROM exercises',
  );
  if (existing[0]?.count > 0) return 0;

  for (const exercise of EXERCISES) {
    await db.execute(
      `INSERT INTO exercises (id, name_vi, name_en, muscle_group, secondary_muscles,
        category, equipment, contraindicated, exercise_type,
        default_reps_min, default_reps_max, is_custom, updated_at)
       VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 0, ?)`,
      [
        exercise.id,
        exercise.nameVi,
        exercise.nameEn,
        exercise.muscleGroup,
        JSON.stringify(exercise.secondaryMuscles),
        exercise.category,
        JSON.stringify(exercise.equipment),
        JSON.stringify(exercise.contraindicated),
        exercise.exerciseType,
        exercise.defaultRepsMin,
        exercise.defaultRepsMax,
        new Date().toISOString(),
      ],
    );
  }

  return EXERCISES.length;
}
