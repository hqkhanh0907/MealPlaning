// ── Build lookup maps (case-insensitive, trimmed) ──────────────────────
function buildMap(entries: DictionaryEntry[], keyIdx: 0 | 1, valIdx: 0 | 1): Map<string, string> {
  const map = new Map<string, string>();
  for (const entry of entries) {
    const key = entry[keyIdx].toLowerCase().trim();
    if (!map.has(key)) {
      map.set(key, entry[valIdx]);
    }
  }
  return map;
}

const viToEn = buildMap(RAW_ENTRIES, 0, 1);
const enToVi = buildMap(RAW_ENTRIES, 1, 0);

/**
 * Look up instant translation for a food term.
 * Returns the translated text, or `null` if not found in the dictionary.
 */
export function lookupFoodTranslation(
  text: string,
  direction: 'vi-en' | 'en-vi',
): string | null {
  const key = text.toLowerCase().trim();
  if (!key) return null;
  const map = direction === 'vi-en' ? viToEn : enToVi;
  return map.get(key) ?? null;
}
